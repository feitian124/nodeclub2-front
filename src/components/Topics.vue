<template>
  <div>
    <div class="loading" v-if="loading">
      Loading...
    </div>

    <div v-if="error" class="error">
      {{ error }}
    </div>

    <div v-if="topics" class="content">
      <p>{{ topics }}</p>
    </div>
  </div>
</template>

<script>
import $ from 'webpack-zepto'
export default {
  data () {
    return {
      loading: false,
      topics: null,
      error: null
    }
  },
  created () {
    // 组件创建完后获取数据，
    // 此时 data 已经被 observed 了
    this.fetchData()
  },
  watch: {
    // 如果路由有变化，会再次执行该方法
    '$route': 'fetchData'
  },
  methods: {
    fetchData () {
      this.error = this.topics = null
      this.loading = true
      $.get('/api/topics', (data) => {
        if (data) {
          this.topics = data
          this.loading = false
        } else {
          this.topics = true
        }
      })
    }
  }
}
</script>

